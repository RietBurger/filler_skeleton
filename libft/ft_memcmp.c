#include "libft.h"

int		ft_memcmp(const void *str1, const void *str2, size_t n)
{
	size_t			i;
	int				diff;
	unsigned char	*s1;
	unsigned char	*s2;

	i = 0;
	s1 = (unsigned char *)str1;
	s2 = (unsigned char *)str2;
	while (n > i)
	{
		diff = s1[i] - s2[i];
		if (diff)
			return (diff);
		i++;
	}
	return (0);
}
